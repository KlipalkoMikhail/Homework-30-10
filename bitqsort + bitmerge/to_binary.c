// функция для перевода числа в двоичный код
// в качестве параметров используюется величина числа и счетчик битов

int to_binary(int n, int *k)
{
    int r;                    // для остатка от деления на 2
    if (n == 0) return *k;    // частный случай, где нет битов
    if (n < 0)                // в отрицательных числах на один бит больше, этот случай мы тоже рассматриваем
    {
        *k += 1;              // повышаем число битов на 1
        to_binary(-1*n, k);
    }
    r = n % 2;                // остаток от деления на 2
    if (n >= 2)
        to_binary(n / 2, k);  // рекурсия, делим на 2 пока величина числа не дойдет до крайнего значения
    if (r == 1) *k += 1;      // повышаем число битов на 1
    return *k;                // возращаем число битов
}
