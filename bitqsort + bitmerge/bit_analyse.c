// функция bit_analyse принимает в качестве параметра числовое значение и изменяет его на количество битов в данном числе

int bit_analyse(int *a)
{
    int value;                                  // числовое значение
    int result = 0;                             // счетчик битов

    value = *a;                                 // сохраняем входящую величину числа
    to_binary(value, &result);                    // передаем величина этого числа и показатели счетчика битов
    *a = result;                                // присваиваем по адресу a значение счетчика

    return result;                              // возвращаем число битов
}
