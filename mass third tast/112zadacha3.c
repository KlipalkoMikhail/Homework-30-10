// Циклический сдвиг массива вправо

#include <stdio.h>

#define N 2000

// 112 Клипалко Михаил Михайлович

int dataprocessing(double number[], int count);
int main(void)
{
    char filename[120];
    double number[N]; // массив на N чисел
    int count = 0; // счетчик чисел

    printf("Shifter of the sequence\n");
    printf("Enter the file name: ");
    scanf("%s", filename);

    FILE * file = fopen(filename, "r");
    if(!file)
    {
        printf("File is not existing\n");
        return -1;
    }
    else
    {
        printf("File has been opened\n");
    }

    printf("Starting sequence:\n");
    while (fscanf(file, "%lf", &number[count]) == 1) // записываем числа в массив
    {
        printf("%lf ", number[count]); // выводим последовательность на экран
        count++;
        if ( count > N )
        {
            printf("File has too many numbers, last number of the sequence is %lf\n", number[count - 1]); // слишком много чисел в файле
            break;
        }
    }
    if (count == 0)
    {
        printf("ERROR: File is empty\n"); // защита от пустого файла
        return -1;
    }
    if (feof(file) == 0) // защита от некорректных символов в последовательности
    {
        printf(" ERROR: File has invalid numbers\n");
        return -1;
    }

    printf("\n");
    printf("\n");
    printf("Shifted sequence:\n");

    dataprocessing(number, count);

    for (int i = 0; i < count; ++i) // цикл распечатки чисел
    {
        printf("%lf ", number[i]);
    }

    return 0;
}

// 112 Клипалко Михаил Михайлович

int dataprocessing(double number[], int count)
{
    double savenumber; // переменная под первое число последовательности

    savenumber = number[count - 1]; // сохраняем последнее значенив массиве
    while ( count > 1) // цикл сдвига
    {
        number[count - 1] = number[count - 2]; // сдвигаем массив вправо
        count -= 1;
    }
        number[0] = savenumber; // присваиваем значение последнего члена в массиве первому члену

    return 0;
}
